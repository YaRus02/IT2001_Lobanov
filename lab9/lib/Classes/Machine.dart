class CoffeeMachine {
  int _coffeeBeans;
  int _milk;
  int _water;
  int _cash;

  CoffeeMachine(this._coffeeBeans, this._milk, this._water, this._cash);

  int get coffeeBeans {
    return _coffeeBeans;
  }

  int get milk {
    return _milk;
  }

  int get water {
    return _water;
  }

  int get cash {
    return _cash;
  }

  
}
